package chap04.lecture.switchcase;

public class C02Switch {
	public static void main(String[] args) {
		System.out.println("smtm  1");

		switch (3) {
		case 1:
			System.out.println("smtm  2");
			break;
		case 2: 
			System.out.println("smtm  3");
			System.out.println("smtm  4");
			break;
		case 3:
			System.out.println("smtm  5");
			System.out.println("smtm  6");
			break;
		}
		System.out.println("smtm  7");
		System.out.println("smtm  8");
		System.out.println("smtm  9");
	}
}
